# almost anything as key in python in a dictionary

kuchbhi = {
	'army': 'the best',
	'pee-kay': 'gir pade',
	1: '2 ka 4',
	True: 'Earth is a planet',
	False: 'Mango is a vegetable',
	('True', 'False'): 'Boolean Type'
}

print (kuchbhi['army'])
print (kuchbhi['pee-kay'])
print (kuchbhi[('True', 'False')])
print (kuchbhi[1])
print (kuchbhi[False])
print (kuchbhi[True])

